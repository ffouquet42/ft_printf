#include "ft_printf.h"

int	ft_nbrlen(int n)
{
	long	nb;
	int		i;
	
	nb = n;
	i = 0;
	if (nb < 0)
	{
		nb *= -1;
		i++;
	}
	if (nb == 0)
		return (1);
	while (nb > 0)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
